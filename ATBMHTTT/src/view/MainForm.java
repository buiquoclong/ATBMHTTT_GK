    private void jButton96ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton96ActionPerformed
        int messageType = JOptionPane.WARNING_MESSAGE;
        String text = jTextArea73.getText();
        String text2 = jTextField15.getText();
        boolean isChecked = jCheckBox11.isSelected();
        boolean isValidPath1 = Paths.get(text).isAbsolute();
        boolean isValidPath2 = Paths.get(text2).isAbsolute();
        if (isChecked) {
            // laf ma hoa file
            if ((text == null || text.isEmpty())) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đường dẫn của file cần mã hóa", "Cảnh báo", messageType);
            } else if (!isValidPath1) {
                JOptionPane.showMessageDialog(rootPane, "Đường dẫn của file không hợp lệ", "Cảnh báo", messageType);
            } else {
                if ((text2 == null || text2.isEmpty())) {
                    String hashMD2;
                    try {
                        hashMD2 = MD2.hashFile(text);
                        jTextArea76.setText(hashMD2);
                    } catch (Exception ex) {
                        Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    if (!isValidPath2) {
                        JOptionPane.showMessageDialog(rootPane, "Đường dẫn của file không hợp lệ", "Cảnh báo", messageType);
                    } else if (text.equals(text2)) {
                        JOptionPane.showMessageDialog(rootPane, "Đường dẫn trùng nhau", "Cảnh báo", messageType);
                    } else {
                        boolean hashMD2;
                        try {
                            hashMD2 = MD2.hashToFile(text, text2);
                            if (hashMD2) {
                                jTextArea76.setText("Hash file thành công" + "\n" + "Đường dẫn file được hash: " + text2);
                            } else {
                                jTextArea76.setText("Hash file thất bại");
                            }
                        } catch (Exception ex) {
                            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }
        } else {
            // neeus khoong phair ma hoa file
            if (text == null || text.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập thông tin cần mã hóa", "Cảnh báo", messageType);
            } else {
                String hashMD2 = MD2.hash(text);
                jTextArea76.setText(hashMD2);
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton96ActionPerformed

